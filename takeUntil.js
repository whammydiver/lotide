const takeUntil = function(array, callback) {
  results = [];
  for (item of array) {
    if (!callback(item)) {
      results.push(item);
    } else {
      return results;
    }
  }
}

const eqArrays = function(array1, array2) {
  if (array1.length !== array2.length) {
    return false;
  }
  for (let x = 0; x < array1.length; x++) {
    if (array1[x] !== array2[x]) {
      return false;
    }
  }
  return true;
}

